package com.example.spring;

public class WhatIsSpring {

	/*
	 * Spring é uma estrutura para Inversão de Dependências. Trata-se de um container
	 * que gerecia o ciclo de vida dos objetos.
	 *
	 * SPRING CONTEXT para injeção de dependências;
	 * SPRING JDBC para acesso a banco de dados;
	 * SPRING ORM para suporte a ferramentas como Hibernate;
	 * SPRING MVC para aplicação web;
	 *
	 * ANOTAÇÕES IMPORTANTES:
	 * @Component: marca uma classe como um componente gerenciável Spring.
	 * @Service: marca uma classe como uma classe de serviço Spring.
	 * @Repository: marca uma classe como repositório de conexão ao BD.
	 * @Controller: marca uma classe como um controller de requisições HTTP.
	 * @Bean: marca um método como uma fábrica de beans gerenciáveis pelo Spring.
	 * @Autowired: maca um construtor, campo, método setter ou parâmetro como sendo
	 * automaticamente injetado pelo Spring.
	 * @Configuration: marca uma classe como sendo classe de configuração do Spring.
	 */
}
