package com.example.spring;

public class Beans {

	/*
	 * Beans in Spring are objects that are instantiated, assembled, and managed by
	 * the Spring container.
	 *
	 * SCOPES:
	 * - Singleton: A single instance of a bean is created and shared by all
	 * applications.
	 * - Prototype: A new instance of a bean is created for each application.
	 * - Request: A new instance of a bean is created for each request.
	 * - Session: A new instance of a bean is created for each user session.
	 * - Global Session: A new instance of a bean is created for each user session
	 * and is shared by all applications.
	 *
	 * Beans are added through XML configs, Java Annotations or
	 * Java code.
	 */
}
