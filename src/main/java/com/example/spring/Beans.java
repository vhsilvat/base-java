package com.example.spring;

public class Beans {

	/*
	 * Em Spring, beans são objetos instanciados, montados e gerenciados pelo
	 * contâiner Spring.
	 *
	 *
	 * ESCOPOS:
	 *
	 * - Singleton: Uma única instância de um bean é criada e compartilhada por todas
	 * as aplicações.
	 *
	 * - Prototype: Uma nova instância de um bean é criada para cada solicitação.
	 *
	 * - Request: Uma nova instância de um bean é criada para cada solicitação.
	 * Válido apenas no contexto web.
	 *
	 * - Session: Uma nova instância de um bean é criada para cada sessão do usuário.
	 * Válido apenas no contexto web.
	 *
	 * - Application (ou Global) Session: Uma nova instância de um bean é criada para
	 * cada sessão do usuário sendo compartilhada por todas as aplicações.
	 * Válido apenas no contexto web.
	 *
	 * Beans are added through XML configs, Java Annotations or
	 * Java code.
	 */
}
