package com.example.oppconcepts;

public class EncapsulationExample {

	/*
	 * Encapsulation is the process of wrapping the data and methods together
	 * to hide the implementation details.
	 *
	 * Encapsulation is achieved by:
	 * 		- Making the fields (parameters) private
	 * 		- Making the getters and setters public
	 * 		- Making the constructors private
	 * 		- Making the methods private
	 *
	 * Essentially, encapsulation provides a protective shield that prevents
	 * data from being accessed by any code that exists outside the shield.
	 */
}
