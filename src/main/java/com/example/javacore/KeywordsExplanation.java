package com.example.javacore;

public class KeywordsExplanation {

	public static void main(String[] args) {
		// public is an access modifier. It means that the method can be accessed
		// by any other class.

		// static is a modifier. It means that the method can be accessed without
		// creating an object of the class.

		// void is a return type. It means that the method doesn't return any
		// value.

		// main is a method name. It means that the method is the main method of
		// the class. The JVM searches for the main method and executes it at
		// the start of the program.

		// String[] args is a parameter. It means that the method takes an array
		// of strings as a parameter. These arguments are aka Java command line
		// arguments.
	}
}
