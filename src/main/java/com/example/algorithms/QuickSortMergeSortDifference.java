package com.example.algorithms;

public class QuickSortMergeSortDifference {

	/*
	 * Both quicksort and mergesort are sorting algorithms that allow array elements
	 * to be arranged in sequence order.
	 */

	/*
	 * In quicksort, each element of the array is compared with the pivot element.
	 *
	 * Quicksort is ideal for sorting small arrays.
	 *
	 * The worst case time complexity of quicksort is O(n^2).
	 *
	 * It doesn't take up any more space than the original array to perform the
	 * sorting.
	 *
	 * For small data sets, it will be faster than mergesort or any other sorting
	 * algorithm.
	 */

	/*
	 * In mergesort, the array is split into two halves and the two halves are
	 * sorted recursively.
	 *
	 * Mergesort is suitable for sorting any array size, whether it is small or
	 * large.
	 *
	 * The worst case time complexity of mergesort is O(n log(n)).
	 *
	 * To merge two subarrays takes additional space as a temporary array.
	 *
	 * With this algorithm, all data sets are sorted at the same speed.
	 */
}
