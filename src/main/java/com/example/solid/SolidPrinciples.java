package com.example.solid;

public class SolidPrinciples {

	/*
	 * SOLID PRINCIPLES:
	 *  - Single Responsibility Principle: A class should have only one reason to
	 * change.
	 * - Open/Closed Principle: A class should be open for extension, but closed
	 * for modification.
	 * - Liskov Substitution Principle: A class should be substitutable for its
	 * subclasses.
	 * - Interface Segregation Principle: A class should not have too many
	 * dependencies on other classes.
	 * - Dependency Inversion Principle: High-level modules should not depend on
	 * low-level modules. Both should depend on abstractions.
	 *
	 *
	 * - Principle of Least Knowledge: A class should only depend on other classes
	 * that it uses.
	 * - Principle of Least Authority: A class should only depend on other classes
	 * that it uses.
	 * - Principle of Least Codding: A class should only depend on other classes
	 * that it uses.
	 */
}
